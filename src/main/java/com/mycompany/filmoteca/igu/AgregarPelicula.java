    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        List<Anio> listaAnios = control.traerAnios();
        if(listaAnios != null){
            for(Anio actual : listaAnios)
               cmbAnio.addItem(String.valueOf(actual.getAnio()));               
        }
        
        List<Genero> listaGeneros = control.traerGeneros();
        if(listaGeneros != null){
            for(Genero actual : listaGeneros)
                cmbGenero.addItem(actual.getNombre());
        }
        
        List<Pais> listaPaises = control.traerPaises();
        if(listaPaises != null){
            for(Pais actual : listaPaises)
                cmbPais.addItem(actual.getNombre());
        }
        
        List<Director> listaDirectores = control.traerDirectores();
        if(listaDirectores != null){
            for(Director actual : listaDirectores)
                cmbDirector.addItem(actual.getNombre());
        }
        
    }//GEN-LAST:event_formWindowOpened

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        int anio = Integer.parseInt((String) cmbAnio.getSelectedItem());
        String director = (String) cmbDirector.getSelectedItem();
        String pais = (String) cmbPais.getSelectedItem();
        String genero = (String) cmbGenero.getSelectedItem();
        int calificacion = Integer.parseInt((String) cmbCalificacion.getSelectedItem());
        
        if(nombre.isBlank())
            mostrarMensaje("El nombre esta en blanco", "Error", "Error");
        else{
            control.agregarPelicula(nombre, anio, director, pais, genero, calificacion);
            mostrarMensaje("Pelicula agregada", "Info", "Exito");
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    public void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    

